
# *Operator Python
# +, -, *, /, //, **, %
P = 10.3
L = 5.44
# pembagian
print(P / L);
# pembagian Pembulatan
print(P // L);
# pangkat
print(P ** L);
# sisa bagi
print(P % L);

# * Operator Pembanding
# ==, !=, >, <, >=, <=
# Tidak Sama Dengan
print(10 != 1)
# Lebih Besar Dari
print(10 > 1)
# Lebih Kecil Dari
print(10 < 1)
# Lebih Besar Dari Atau Sama Dengan
print(10 >= 1)
# Lebih Kecil Dari Atau Sama Dengan
print(10 <= 1)
