# * if else
nilai = 80
if nilai >= 80:
    print("Selamat, Anda Lulus")
else:
    print("Maaf, Anda Tidak Lulus")

# ! Perbedaan >= dengan >
# >= saat nilai itu pas
# > saat nilai itu tidak pas atau melebihi nilai ynag ditentukan

